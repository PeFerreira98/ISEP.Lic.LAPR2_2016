    private void btnAceitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceitarActionPerformed
        String decisao = "Aceite";
        String justificacao = jTextArea1.getText();
        if (justificacao.equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(AvaliarCandidaturaUI.this,
                    "Adicione uma justificação", "Avaliar candidatura", JOptionPane.ERROR_MESSAGE);

        } else {
            this.controller.avaliarCandidatura(decisao, justificacao);
        }

    }//GEN-LAST:event_btnAceitarActionPerformed

