    private void inicializarLista() {
        final List<Stand> list = this.controller.getStands();

        if (list.isEmpty()) {
            this.jLstStands.setModel(new DefaultListModel<>());
            JOptionPane.showMessageDialog(this, "NÃ£o existem stands");
        }

        DefaultListModel listModel = new DefaultListModel() {
            @Override
            public int getSize() {
                return list.size();
            }

            @Override
            public Object getElementAt(int i) {
                return list.get(i);
            }
        };

        this.jLstStands.setModel(listModel);
        this.jLstStands.setCellRenderer(new CellRenderer());
    }

    private class CellRenderer extends JLabel implements ListCellRenderer<Stand> {

        public CellRenderer() {
            setOpaque(true);
        }

        @Override
        public Component getListCellRendererComponent(JList<? extends Stand> list, Stand stand, int index, boolean isSelected, boolean cellHasFocus) {
            setText(stand.getDesignacao());

            if (isSelected) {
                setBackground(list.getSelectionBackground());
                setForeground(list.getSelectionForeground());
            } else {
                setBackground(list.getBackground());
                setForeground(list.getForeground());
            }

            return this;
        }
    }
    
    

