    private void inicializarLista() {
        final List<Retiravel> listaRetiraveis = this.controller.getListaRetiraveis();

        if (listaRetiraveis.isEmpty()) {
            this.jListRetiraveis.setModel(new DefaultListModel<>());
            JOptionPane.showMessageDialog(this, "NÃ£o existem Retiraveis");
        }

        DefaultListModel listModel = new DefaultListModel() {
            @Override
            public int getSize() {
                return listaRetiraveis.size();
            }

            @Override
            public Object getElementAt(int i) {
                return listaRetiraveis.get(i);
            }
        };

        this.jListRetiraveis.setModel(listModel);
        this.jListRetiraveis.setCellRenderer(new CellRenderer());
    }

    private class CellRenderer extends JLabel implements ListCellRenderer<Retiravel> {

        public CellRenderer() {
            setOpaque(true);
        }

        @Override
        public Component getListCellRendererComponent(JList<? extends Retiravel> list, Retiravel retiravel, int index, boolean isSelected, boolean cellHasFocus) {
            setText(retiravel.getNomeEmpresa());

            if (isSelected) {
                setBackground(list.getSelectionBackground());
                setForeground(list.getSelectionForeground());
            } else {
                setBackground(list.getBackground());
                setForeground(list.getForeground());
            }

            return this;
        }

    }

